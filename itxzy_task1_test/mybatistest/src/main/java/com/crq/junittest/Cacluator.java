public class Cacluator {
    /**
     * @param a
     * @param b
     * @return sum
     * */
    public int sum(int a,int b){
        return a+b;
    }

    /**
     * @param a
     * @param b
     * @return a-b
     * */
    public int sub(int a,int b){
        return a-b;
    }

    /**
     * @param a
     * @param b
     * @return a*b
     *
     * */
    public int mix(int a,int b){
        return a*b;
    }
}
